				<p class="data">Если вы не хотите менять изображение или звук - оставьте эти поля пустыми.</p>
	
				<a class="anchor" name="18"></a>
				<p class="title">18. Как работает поиск?</p>
				<p class="data">По нажатию на иконку лупы в шапке/боковом меню сайта откроется окно с полем ввода, куда необходимо ввести поисковый запрос. По умолчанию поиск ведется по заголовкам постов. </p>
				<p class="data">Также присутствует расширенная версия поиска, попасть на которую можно, выполнив предыдущий пункт с обычным поиском, либо нажав на эмоцию или тег под постом. Страница расширенного поиска находится по данному адресу: http://piqtune.com/search</p>
				<p class="data">Для того, чтобы начать расширенный поиск, необходимо ввести данные в одно из трех доступных полей: поиск по заголовку, поиск по тегам, поиск по эмоциям. Также возможно их совмещение для получения более точных результатов.</p>
	
